public class HamP {
	
	String 햄버거이름;
	String 소스;
	String 빵;
	String 패티;
	String 야채;
	
	public HamP() {} 
	
//햄버거의내용물선택	
	public HamP(String 햄버거이름, String 소스, String 빵, String 패티, String 야채) {
		super();
		this.햄버거이름 = 햄버거이름;
		this.소스 = 소스;
		this.빵 = 빵;
		this.패티 = 패티;
		this.야채 = 야채;

	}


}
